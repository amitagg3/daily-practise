    public static int countCharacters(String[] words, String chars) {
        int count = 0;
        Map<Character, Integer> charCount = new HashMap<>();
        for (Character c : chars.toCharArray()) {
            charCount.put(c, charCount.getOrDefault(c, 0) + 1);
        }
        for (String word : words) {
            Map<Character, Integer> specificWordCharCount = new HashMap<>();
            boolean canForm = true;
            for (Character c : word.toCharArray()) {
                if (!charCount.containsKey(c) || Objects.equals(charCount.get(c), specificWordCharCount.get(c))) {
                    canForm = false;
                    break;
                }
                specificWordCharCount.put(c, specificWordCharCount.getOrDefault(c, 0) + 1);
            }
            if (canForm)
                count += word.length();
        }
        return count;
    }
